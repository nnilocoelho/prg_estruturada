valor1 = bool(input("Insira o primeiro valor ou aperte ENTER: "))
valor2 = bool(input("Insitra o segundo valor ou aperte ENTER: "))

if valor1 == valor2:
    print(f"Valor 1 é {valor1} e o Valor 2 é {valor2}")

elif valor1 != valor2:
    print (f"Valor 1 é {valor1} e o Valor 2 é {valor2}")

else:
    print (f"Valor 1 é {valor1} e o Valor 2 é {valor2}")